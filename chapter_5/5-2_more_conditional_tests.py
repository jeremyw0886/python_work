# Numerical tests
number = 10

# Equality (True)
print("\nIs number == 10? I predict True.")
print(number == 10)

# Inequality (False)
print("\nIs number == 5? I predict False.")
print(number == 5)

# Greater than (True)
print("\nIs number > 5? I predict True.")
print(number > 5)

# Less than (False)
print("\nIs number < 5? I predict False.")
print(number < 5)

# Greater than or equal to (True)
print("\nIs number >= 10? I predict True.")
print(number >= 10)

# Less than or equal to (False)
print("\nIs number <= 5? I predict False.")
print(number <= 5)

# Test using the and keyword (True)
print("\nIs number > 5 and number < 15? I predict True.")
print(number > 5 and number < 15)

# Test using the or keyword (False)
print("\nIs number < 5 or number > 15? I predict False.")
print(number < 5 or number > 15)

# Test whether an item is in a list (True)
